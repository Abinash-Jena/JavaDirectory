public class BItwise {
    public static void main(String[] args) {
        System.out.println(10 & 8);
        System.out.println(10 | 8);
        System.out.println(10 ^ 8);
        System.out.println(~10);
        System.out.println(~ -11);
        System.out.println(4<<3);
        System.out.println(16>>2);
    }
}
