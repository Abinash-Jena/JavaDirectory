public class Relational {
    public static void main(String[] args) {
        System.out.println(5 == 5);
        System.out.println(5 != 5);
        System.out.println(4 > 5);
        System.out.println(5 > 5);
        System.out.println(6 > 5);
        System.out.println(4 < 5);
        System.out.println(5 < 5);
        System.out.println(6 < 5);
        System.out.println(4 >= 5);
        System.out.println(5 >= 5);
        System.out.println(6 >= 5);
        System.out.println(4 <= 5);
        System.out.println(5 <= 5);
        System.out.println(6 <= 5);
    }
}
